import math  # Lấy nội dung của thư viện math về sử dụng

# 1. Hàm lấy phần nguyên (bỏ phần thập phân)
print(math.trunc(3.9))  # Kết quả: 3

# 2. Hàm lấy giá trị tuyệt đối (trả về số thực)
print(math.fabs(-3))  # Kết quả: 3.0

# 3. Hàm tính căn bậc hai
print(math.sqrt(16))  # Kết quả: 4.0

# 4. Hàm tìm ước chung lớn nhất (UCLN)
print(math.gcd(6, 4))  # Kết quả: 2