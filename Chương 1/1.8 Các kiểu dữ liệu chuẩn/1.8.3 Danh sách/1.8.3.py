# Giả sử nhập vào một xâu có đúng 12 ký tự (Ví dụ: "HocPython_12")
s = "HocPython_12"

# 1. In ra ký tự thứ 3 (Chỉ mục là 2)
print("Ký tự thứ 3 là:", s[2])

# 2. In ra từ ký tự thứ 4 đến thứ 7 (Chỉ mục từ 3 đến 6 -> s[3:7])
print("Ký tự thứ 4 đến 7 là:", s[3:7])

# 3. In ra từ ký tự thứ 11 đến hết xâu (Chỉ mục từ 10 đến hết -> s[10:])
print("Ký tự thứ 11 và kết xâu là:", s[10:])