# Ví dụ 1: Loại bỏ toàn bộ khoảng trắng ở phía bên phải chuỗi
Str1 = "   Vi Du Ham rstrip () trong Python   "
print(Str1.rstrip())  # Kết quả: "   Vi Du Ham rstrip () trong Python"

# Ví dụ 2: Loại bỏ ký tự '-' ở phía bên phải chuỗi
