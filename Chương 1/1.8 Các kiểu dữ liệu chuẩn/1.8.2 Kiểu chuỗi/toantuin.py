# Toán tử kiểm tra thành phần (in / not in)
print('a' in 'abc')       # Kết quả: True
print('ab' in 'abc')      # Kết quả: True
print('ac' in 'abc')      # Kết quả thực tế: False (Trong tập ghi nhầm là True)
print('ac' not in 'abc')  # Kết quả thực tế: True  (Trong tập ghi nhầm là False)