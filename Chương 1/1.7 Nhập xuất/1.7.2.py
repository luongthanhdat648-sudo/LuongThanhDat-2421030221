# Cú pháp chung: <tên biến> = input("dòng thông báo")

# Nhập số thực (float) từ bàn phím
a = float(input("a = "))  # Ví dụ nhập vào: 7.4

# Nhập số nguyên (int) từ bàn phím
b = int(input("b = "))    # Ví dụ nhập vào: 5

# Thực hiện phép tính cộng
ket_qua = a + b
print(ket_qua)            # Kết quả hiển thị: 12.4