# Cú pháp: <tên biến> = <giá trị của biến>

# Gán số nguyên cho biến a
a = 1
print(a)  # Kết quả: 1

# Gán lại chuỗi ký tự cho biến a
a = 'Hello World'
print(a)  # Kết quả: Hello World

# Gán lại một danh sách (List) số nguyên cho biến a
a = [1, 2, 3]
print(a)  # Kết quả: [1, 2, 3]

# Gán lại một danh sách hỗn hợp nhiều kiểu dữ liệu khác nhau cho biến a
a = [1.2, 'Hello', 'W', 2]
print(a)  # Kết quả: [1.2, 'Hello', 'W', 2]